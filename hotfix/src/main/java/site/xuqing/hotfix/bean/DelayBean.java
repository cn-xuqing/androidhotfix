package site.xuqing.hotfix.bean;

public class DelayBean {
    private boolean upgradeDelay;
    private boolean hotfixDelay;

    public boolean isUpgradeDelay() {
        return upgradeDelay;
    }

    public void setUpgradeDelay(boolean upgradeDelay) {
        this.upgradeDelay = upgradeDelay;
    }

    public boolean isHotfixDelay() {
        return hotfixDelay;
    }

    public void setHotfixDelay(boolean hotfixDelay) {
        this.hotfixDelay = hotfixDelay;
    }
}
